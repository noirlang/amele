export function translate(language, key, vars = {}) {
  let value = translations[language]?.[key] || translations.tr[key] || key;
  for (const [name, replacement] of Object.entries(vars)) {
    value = value.replaceAll(`{${name}}`, replacement);
  }
  return value;
}
